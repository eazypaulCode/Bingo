package de.eazypaulcode.util;

/**
 * @author eazypaul
 * @since 20.12.2021
 */
public class StringDefaults {

    public static String NORMAL(String paramString) {
        return "§9§l" + paramString + " §8● ";
    }

    public static String WARNING(String paramString) {
        return "§e§l" + paramString + " §8● ";
    }

    public static String ERROR(String paramString) {
        return "§c§l" + paramString + " §8● ";
    }

    public static String SUCCESS(String paramString) {
        return "§a§l" + paramString + " §8● ";
    }

}
